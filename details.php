                        over Brie. Top with remaining bread slices, toasted side 
                        facing inward. Spread mayonnaise mixture on the outsides of 
                        each sandwich. Place in same skillet and cook until bread 
                        is golden brown and cheese is melted, 5-6 minutes on each 
                        side. Serve immediately.</li>
